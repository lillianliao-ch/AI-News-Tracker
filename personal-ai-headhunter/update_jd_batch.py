# 下线的职位
OFFLINE_JOBS = [809, 814, 815]


def main():
    db = SessionLocal()
    
    updated = 0
    
    for job_data in JD_DATA:
        job = db.query(Job).filter(Job.id == job_data["db_id"]).first()
        if job:
            job.title = job_data["title"]
            job.job_code = job_data["job_code"]
            job.location = job_data["location"]
            job.raw_jd_text = job_data["jd_content"]
            print(f"✅ 更新: {job_data['title']} ({job_data['job_code']})")
            updated += 1
    
    # 标记下线职位
    for job_id in OFFLINE_JOBS:
        job = db.query(Job).filter(Job.id == job_id).first()
        if job:
            job.is_active = 0
            job.notes = "职位已下线"
            print(f"🔴 标记下线: {job.title}")
    
    db.commit()
    print(f"\n📊 完成: 更新 {updated} 个职位, 标记下线 {len(OFFLINE_JOBS)} 个")


if __name__ == "__main__":
    main()
