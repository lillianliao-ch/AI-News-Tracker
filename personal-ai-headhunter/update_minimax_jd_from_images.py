def update_minimax_jd():
    """Update existing MiniMax job records with complete JD text from images."""
    db = SessionLocal()
    
    try:
        updated = 0
        not_found = 0
        skipped = 0
        
        for entry in IMAGE_JD_DATA:
            job_code = entry["match_job_code"]
            match_title = entry["match_title"]
            
            # Find the existing job by job_code
            job = db.query(Job).filter(Job.job_code == job_code).first()
            
            if not job:
                # Try by title match
                job = db.query(Job).filter(
                    Job.company == "MiniMax",
                    Job.title.contains(match_title)
                ).first()
            
            if not job:
                print(f"❌ 未找到: [{job_code}] {match_title}")
                not_found += 1
                continue
            
            # Check if JD already has substantial content (>300 chars = likely already enriched)
            current_jd_len = len(job.raw_jd_text or "")
            new_jd_len = len(entry["raw_jd_text"])
            
            if current_jd_len >= new_jd_len and current_jd_len > 300:
                print(f"⏭️  跳过 (JD已完整, {current_jd_len}字): [{job.job_code}] {job.title}")
                skipped += 1
                continue
            
            # Update the job record
            old_title = job.title
            job.raw_jd_text = entry["raw_jd_text"]
            job.location = entry.get("location", job.location)
            
            # Update department if provided and current one is generic
            if entry.get("department"):
                job.department = entry["department"]
            
            # Update title if image shows a different/more accurate one
            if entry.get("update_title"):
                job.title = entry["update_title"]
                
            print(f"✅ 更新: [{job.job_code}] {old_title}")
            print(f"   JD: {current_jd_len} → {new_jd_len} 字符")
            if entry.get("update_title"):
                print(f"   标题更新: {old_title} → {entry['update_title']}")
            updated += 1
        
        db.commit()
        
        print(f"\n{'='*60}")
        print(f"📊 更新统计:")
        print(f"   处理总数: {len(IMAGE_JD_DATA)}")
        print(f"   成功更新: {updated}")
        print(f"   已跳过 (JD已完整): {skipped}")
        print(f"   未找到: {not_found}")
        print(f"{'='*60}")
        
        return updated, skipped, not_found
        
    except Exception as e:
        db.rollback()
        print(f"❌ 更新失败: {e}")
        import traceback
        traceback.print_exc()
        raise
    finally:
        db.close()


if __name__ == "__main__":
    update_minimax_jd()
